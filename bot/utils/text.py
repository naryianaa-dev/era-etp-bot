"""HTML-экранирование для безопасной встройки пользовательского ввода в сообщения с parse_mode=HTML."""

from __future__ import annotations

from html import escape


def h(value: object) -> str:
    """Экранирует значение для безопасной вставки в HTML-сообщение Telegram.

    Бот использует ``parse_mode=HTML`` глобально (см. ``bot/main.py``).
    Любое пользовательское поле (имя, марка, описание, VIN, артикул,
    название/описание оффера и т.д.), которое попадает в шаблон с
    HTML-тегами, должно быть прогнано через ``h()``, иначе атакер может
    закрыть/открыть теги и встроить ссылку — это спуфинг сообщений
    бота для админа.

    ``None`` нормализуется в пустую строку (вместо ``"None"``).
    """
    if value is None:
        return ""
    return escape(str(value), quote=False)
